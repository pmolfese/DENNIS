    static var tensor: EPTensor { EPTensor(dims: dims, data: flat) }

    @Test func twoStepMatchesReference() throws {
        let result = try TwoStepPCA.run(
            tensor: Self.tensor, firstMode: .temporal, secondMode: .spatial,
            firstFactors: 3, secondFactors: 2,
            firstRotation: .unrotated, secondRotation: .unrotated
        )

        // First-step variance shares.
        let expectedFirstVar = [0.23202811, 0.17268318, 0.15198463]
        #expect(setsMatch(result.first.variance, expectedFirstVar, tol: 1e-5))

        // First-step scree.
        let expectedScree = [1.54925682, 1.15300943, 1.01480474, 0.91388446,
                             0.64021213, 0.54113357, 0.47648102, 0.38823985]
        for i in 0..<8 { #expect(abs(result.first.scree[i] - expectedScree[i]) < 1e-5) }

        // Combined factors: 3 temporal × 2 spatial = 6.
        #expect(result.factors.count == 6)
        let expectedCombined = [0.10130428, 0.06937037, 0.10310083,
                                0.03229581, 0.0692511, 0.04722063]
        #expect(setsMatch(result.factors.map(\.variance), expectedCombined, tol: 1e-4))
    }

    @Test func twoStepScreeMatchesReference() throws {
        let analysis = try Scree.analyzeTwoStep(
            Self.tensor, firstMode: .temporal, secondMode: .spatial,
            firstFactors: 3, firstRotation: .unrotated, nRandom: 1, seed: 0
        )
        let expectedScree = [2.44893589, 1.3255401, 0.66346915, 0.38364357, 0.13315704]
        #expect(analysis.dataScree.count == 5)
        for i in 0..<5 { #expect(abs(analysis.dataScree[i] - expectedScree[i]) < 1e-5) }
        #expect(analysis.retainedMinVariance == 3)
    }

    @Test func infomaxProducesFiniteDecorrelatedScores() throws {
        // Promax and Infomax should both yield finite, well-formed solutions.
        let result = try PCACore.doPCA(
            Self.tensor.reshape(forMode: .temporal),
            mode: .temporal, rotation: .infomax, nFactors: 3
        )
        #expect(result.pattern.grid.allSatisfy { $0.isFinite })
        #expect(result.scores.grid.allSatisfy { $0.isFinite })
        // Factor scores are standardized (unit variance) after the engine's
        // final normalization.
        let sd = columnStd(result.scores)
        for value in sd { #expect(abs(value - 1.0) < 1e-6) }
    }

    private func setsMatch(_ a: [Double], _ b: [Double], tol: Double) -> Bool {
        guard a.count == b.count else { return false }
        var remaining = b
        for value in a {
            guard let idx = remaining.firstIndex(where: { abs($0 - value) < tol }) else { return false }
            remaining.remove(at: idx)
        }
        return true
    }
